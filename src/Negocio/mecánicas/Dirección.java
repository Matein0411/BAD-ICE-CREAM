package Negocio.mecánicas;
/**
 * Enumeración de las posibles direcciones en las que algo puede moverse.
 */
public enum Dirección {
    ARRIBA,     // Indica la dirección hacia arriba.
    ABAJO,      // Indica la dirección hacia abajo.
    DERECHA,    // Indica la dirección hacia la derecha.
    IZQUIERDA   // Indica la dirección hacia la izquierda.
}
